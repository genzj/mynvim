---@return string[] # randomly picked header logo
local function random_logo()
    return HEADERS[math.random(#HEADERS)]
end

---@param str string text to be hashed
---@return number # simple hash value derived from the string
local function hash_string(str)
    local hash = 5381
    for i = 1, #str do
        hash = (bit.lshift(hash, 5) + hash) + string.byte(str, i)
        hash = bit.band(hash, 0xFFFFFFFF)
    end
    return hash
end

---@return string[] # pick header logo by using today's date as hash seed
local function daily_logo()
    -- Get today's date as string
    local date_str = tostring(os.date("%Y-%m-%d"))

    -- Calculate hash from the date string
    local hash = hash_string(date_str)

    -- Use hash to select an index
    local index = (hash % #HEADERS) + 1

    return HEADERS[index]
end

return {
    random_logo = random_logo,
    daily_logo = daily_logo,
}
